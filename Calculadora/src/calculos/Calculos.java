package calculos;

public class Calculos {
	
	public double mAdi(double n1, double n2) {
    	double resultado = n1 + n2;
    	return resultado;
    }
	
	public double mSub(double n1, double n2) {
    	double resultado = n1 - n2;
    	return resultado;
    }
	
	public double rQ(double n1) {
    	double resultado = java.lang.Math.sqrt(n1);
    	return resultado;
    }
	
	public double divi(double n1, double n2) {
    	double resultado = n1 / n2;
    	return resultado;
    }
	
	public double diviPorcent(double n1, double n2) {
    	double resultado = n1 / ((n1 * n2) / 100);
    	return resultado;
    }
	
	public double multiPorcent(double n1, double n2) {
    	double resultado = n1 * ((n1 * n2) / 100);
    	return resultado;
    }
	
	public double subPorcent(double n1, double n2) {
    	double resultado = n1 - ((n1 * n2) / 100);
    	return resultado;
    }
	
	public double adiPorcent(double n1, double n2) {
    	double resultado = n1 + ((n1 * n2) / 100);
    	return resultado;
    }
	
	public double multi(double n1, double n2) {
    	double resultado = n1 * n2;
    	return resultado;
    }
	
	public double reciproco(double n1) {
    	double resultado = 1 / n1;
    	return resultado;
    }
	
    public double sub(double n1, double n2) {
    	double resultado = n1 - n2;
    	return resultado;
    }
    
    public double adi(double n1, double n2) {
    	double resultado = n1 + n2;
    	return resultado;
    }
}
